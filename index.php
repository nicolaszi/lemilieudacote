                <h2>NOTE D'INTENTION</h2>
            </div>
            
            <div class="first">
                <div class="row">
                    <div class="col-md-6">
                        <p>
                            A l'origine de ce projet se cache un homme rencontré lors d'un précédent film. J'interrogeais alors celui que l'on m'avait présenté comme la mémoire du barreau de la Seine-Saint-Denis. Cependant ce n'était pas la seule histoire dont était dépositaire Maître Claude Michel, aussi fondateur avec Robert Ballanger de l'APFA (Association des Pays de France et de l'Aulnoye). Une association de loi 1901 ayant pour mission l'accueil, le développement personnel et l'intégration sociale et professionnelle de personnes mentalement handicapées.  
                        </p>
                        
                        <p>
                            Avant de me lancer dans ce projet je n'avais jamais été directement en contact avec des personnes handicapées mentales, ni dans ma famille, ni parmi mes proches. Ce que je pensais savoir s'est avéré être une longue suite de clichés et d'à priori. On a tendance à exprimer, autour de la notion du handicap, des choses de l'ordre de la tristesse, du malheure, de la perte de chance, de la dévalorisation. Que ce soit sur leur capacité d'apprentissage, leur mode de vie ou tout simplement leur comportement, je me suis rapidement rendu compte que j'étais loin de la réalité. Il s'en est fallu de peu. Une rencontre, une visite, se faire guider dans un atelier par un travailleur. Une discussion autour du potager.
                        </p>
                        
                        <p>  
                            Avec ce film je voulais montrer ce qui est peu visible. La vie des personnes handicapées ne fait pas la une des journaux (ce qui n'est sans doute pas un mal), elle reste une inconnue pour la plupart des gens. On en parle lors des grands évènements (handisport, journée nationale...), mais qu'en est-il du quotidien ? Et qu'en est-il de leur place dans la société ? Accordant de l'importance à la force d'une image je pense qu'il est important de montrer, mais aussi de voir et de se voir. Quoi de mieux qu'une caméra venue de l'extérieur et quelqu'un soucieu de vous aider à vous raconter.
                        </p>
                    
                        <p>
                            Le milieur du handicap existe, en tant que milieu social. Comme partout l'individu y tient une grande place, dans son unicité, rendant toute tentative de catégorisation réductrice. Nous finissons par nous demander si l'Autre si différent, n'est-il pas le Même.
                        </p>
